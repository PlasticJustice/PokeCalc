function fixFullWidth(str)
{
	var full = 'ａｂｃｄｅｆＡＢＣＤＥＦ０１２３４５６７８９';
	var regular = 'abcdefABCDEF0123456789';
	full = full.split("");
	regular = regular.split("");
	for (var a = 0, z = full.length; a < z; ++a) {
		str = str.replace(full[a], regular[a]);
	}
	return str;
}

